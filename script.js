const mainTitle1 = document.getElementById("main-title-1st");
const intro = document.getElementById("introduction");
const introDetails = 

document.getElementById("introduction-details");
const mainTitle2 = document.getElementById("main-title-2nd");
const buttons = document.querySelectorAll("#buttons button");
const sTitle = document.getElementById("S-title");
const sText = document.getElementById("S-text");
const sImage = document.getElementById("S-image");
const sDetails = document.getElementById("service-details");
const langButtons = document.querySelectorAll(".lang-btn");

let currentLang = "en";
let currentService = 0;


function setContent(idx) {
    const t = translations[currentLang].services;
    [sTitle, sText, sImage, sDetails].forEach(el => el.classList.add("hidden"));

    setTimeout(() => {
        const service = t[idx];
        sTitle.textContent = service.title;
        sText.textContent = service.text;
        sImage.src = service.img;
        sImage.alt = service.title;
        sDetails.textContent = service.details;
        void sTitle.offsetWidth;
        [sTitle, sText, sImage, sDetails].forEach(el => el.classList.remove("hidden"));
        buttons.forEach(b => b.classList.remove("active"));
        buttons[idx].classList.add("active");
        currentService = idx;

    }, 400);
}

function updateTexts() {
    const t = translations[currentLang];
    mainTitle1.textContent = t.mainTitle1;
    intro.innerHTML = t.introduction;
    introDetails.innerHTML = t.introductionDetails;
    mainTitle2.textContent = t.mainTitle2;
    buttons.forEach((btn, i) => {
        const span = btn.querySelector("span");
        if (!span) return;
        span.textContent = t.serviceButtons[i];
        if (currentLang === "de" && t.serviceButtons[i] === "Programmierung") {
            span.style.fontSize = "0.8rem";
        } else {
            span.style.fontSize = "1rem";
        }
    });

    setContent(currentService);
}

buttons.forEach((btn, idx) => {
    btn.addEventListener("click", () => setContent(idx));
});

[sImage].forEach(img => {
    img.addEventListener("mouseenter", () => {
        sImage.style.transform = "scale(1.1)";
        sText.style.transform = "scale(0.95)";
        sDetails.style.transform = "scale(0.95)";
    });
    img.addEventListener("mouseleave", () => {
        sImage.style.transform = "scale(1)";
        sText.style.transform = "scale(1)";
        sDetails.style.transform = "scale(1)";
    });

});

[sImage, sText, sDetails].forEach(el => {
    el.style.transition = "transform 0.3s ease, opacity 0.4s ease";
});

langButtons.forEach(btn => {
    btn.addEventListener("click", () => {
        currentLang = btn.dataset.lang;
        currentService = 0;
        updateTexts();
    });
});

updateTexts();
